    private void SHOWMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_SHOWMouseClicked
        // TODO add your handling code here:
         SimpleDateFormat dateformat=new SimpleDateFormat("dd/MM/YYYY");
        String date1=dateformat.format(FROM.getDate());
        String date2=dateformat.format(TO.getDate());
        String query="select * from customer where date between ? and ?";
        try {
            pst=conn.prepareStatement(query);
            pst.setString(1,date1);
            pst.setString(2,date2);
            rs=pst.executeQuery();
            jcustomertable.setModel(DbUtils.resultSetToTableModel(rs));
            if(rs.next())
            {
                rs.close();
                pst.close();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_SHOWMouseClicked

