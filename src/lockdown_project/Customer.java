    private void jcustomertableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jcustomertableMouseClicked
       try{
            int row=jcustomertable.getSelectedRow();
            String s=(jcustomertable.getModel().getValueAt(row,0).toString());
            String query="select * from customer where C_ID='"+s+"'";
            pst=conn.prepareStatement(query);
           
            rs=pst.executeQuery();
            if(rs.next()){
                String add1=rs.getString("C_ID");
                txtId.setText(add1);
                String add2=rs.getString("FIRST_NAME");
                TxtFirst.setText(add2);
                String add3=rs.getString("LAST_NAME");
                TxtLast.setText(add3);
                String add4=rs.getString("ADDRESS");
                TxtAddress.setText(add4);
                String add5=rs.getString("PHONE");
                TxtPhone.setText(add5);
                String add6=rs.getString("EMAIL");
                TxtEmail.setText(add6);
                String add7=rs.getString("DATE");
                TxtDate.setText(add7);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        Update_table();
        
    }//GEN-LAST:event_jcustomertableMouseClicked

